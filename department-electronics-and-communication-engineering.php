                    <h3 style="margin-top:30px;">About the Department</h3>

                    <hr>

                </div>

                <p align="justify">The Department of Electronics and Communication Engineering (ECE) is one of the major Departments of the institution since its inception in the year 1998. The department is accredited by NBA in 2003 and reaccredited in 2009. The department is very active in Teaching and Research in the areas of Communications and Signal Processing, VLSI and Nano Technology. The Department is well equipped with the state of the art facilities for carrying out Industry sponsored and Consultancy Research and Development (R&D) projects in various fields. Global Excellence in Teaching, Research and Technology development is the main concern of the Department. It has renowned as well as young and dynamic personalities as faculty and involved in providing Quality Education at both Undergraduate (UG) and Postgraduate (PG) levels. This department offers B.E (ECE) with an intake of 120 at UG level and M.E (Applied Electronics) with an intake of 18 seats at PG level. The department is a recognized Research Center of Anna University and hence offers high quality research in both M.S (By Research) and Ph.D programmes.</p>
                <p align="justify">The top 10 students of the department are selected for Employability Enhancement Training Programme by BSNL-RTTC, sponsored by AICTE, New Delhi. The department has latest softwares like Matlab, CADENCE , Xilinx and Opnet. The department adds to the quality of education by teaching several subjects covering fundamentals and advanced topics in the field of electronics & communication by regularly conducting International and National Conferences, Workshops and Seminar, symposia on latest advancements in technology.</p>
                <p align="justify">The department faculties have several publications in reputed Journals with impact factor to their credit. The students of the department have an excellent placement record and have been consistently above 90 % for the past several years. Over the years, the graduating students have gone on to occupy key positions in core industries, software, education and government administration in India and abroad. </p>

