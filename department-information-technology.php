                <palign="justify">The department of Information Technology was started in 1998. Initially the department was started with an intake of 40 students per year which has grown to 120 students per year. Department is currently headed by Dr. S.Surendran. Each and every faculty member is well qualified with excellent experience in teaching and research. The young and energetic Faculty teams are highly dedicated in their academic activities. The department got NBA accreditation in 2009. Various value added programs have been included to enable the students equip themselves with the latest technology.</p>

                <p align="justify">Our department strives in imparting quality education by providing excellent academic as well as hands on experience in the practical field. The department provides latest hardware and software facilities along with the newest technology laboratory and smart classroom. The department organizes various guest lectures, seminars, workshops, symposiums, faculty development programs, national and international conferences, to help both students and the staff members to keep themselves up to date with latest developments in the information technology field. Industrial visits to various organizations related to the academic field are organized every semester by the department. The department results have been consistent and improving each year due to outstanding performance from both the student and faculty side. </p>


