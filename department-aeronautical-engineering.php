                <p class="lead" align="justify">To cater to the growing need in Aeronautical Engineering education, TEC has started, as a first step, a 4-year UG programme from this academic year. AICTE has accorded approval and this BE course is affiliated to Anna University, Chennai.</p>

                <p align="justify">Currently there are four members of staff. The HOD and two of the Professors are Ph.D. holders. The B.E., Aeronautical Engineering Programme envisaged will have its accent on the Research and Development aspects of Aerospace activities in our country and abroad. Equal stress is laid on the three broad areas namely, Airplane structures, Aerodynamics and Airplane Propulsion. Importance is given to Airplane Design and student project work. Field trips / visits to Aeronautical and space establishments in the neighbourhood in our country have been planned as part of the curriculum. The Syllabus content pertains to the R&D training of students as defined by the Anna University.</p>

                <p align="justify">Training in special areas such as Computational Fluid Dynamics, Finite Element Methods and use of specialized analysis software are also included.A separate building for Aeronautical Engineering Department has been constructed comprising aerodynamics, structures and propulsion laboratories, classrooms, staff rooms and hangar space for airplanes and engines.</p>



